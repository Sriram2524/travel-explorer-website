export const getPhotosForDestination = (destination: string, count: number = 9) => {
  const normalizedDestination = destination.toLowerCase().trim();
  
  // Check for exact matches first
  if (destinationPhotos[normalizedDestination as keyof typeof destinationPhotos]) {
    const photos = destinationPhotos[normalizedDestination as keyof typeof destinationPhotos];
    return photos.slice(0, count);
  }
  
  // Check for partial matches
  const destinationKeys = Object.keys(destinationPhotos);
  const matchingKey = destinationKeys.find(key => 
    key.includes(normalizedDestination) || normalizedDestination.includes(key)
  );
  
  if (matchingKey) {
    const photos = destinationPhotos[matchingKey as keyof typeof destinationPhotos];
    return photos.slice(0, count);
  }
  
  // Return a random selection for unknown destinations
  const shuffled = [...mockPhotos].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, 6));
};

